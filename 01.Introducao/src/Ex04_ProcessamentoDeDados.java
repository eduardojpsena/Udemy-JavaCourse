
public class Ex04_ProcessamentoDeDados {

	public static void main(String[] args) {

		double b, B, h, area;	//declarando variáveis como numero flutuante
		
		b = 6.0;
		B = 8.0;
		h = 5.0;	//armazenando os valores nas variáveis
		
		area = (b + B) / 2.0 * h;	//armazenando a expressão na variável
		
		System.out.println(area);
	}

}